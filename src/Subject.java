public class Subject {
    private String code;
    private String name;

    public Subject(String code, String name) {
        this.code = code;
        this.name = name;
    }

    // code 필드의 getter 메서드
    public String getCode() {
        return code;
    }


    // name 필드의 getter 메서드
    public String getName() {
        return name;
    }

   
}
